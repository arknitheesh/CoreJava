package org.DataTypes;

public class DataType {

	public static void main(String[] args) {
		
		//Premitive Data Type
		byte b = 127; //Stores whole numbers from -128 to 127
		short sh = 654; //Stores whole numbers from -32,768 to 32,767
		int in = 123 ; //Stores whole numbers from -2,147,483,648 to 2,147,483,647
		long lng = 1247765658687l; //Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		float fl = 331.67f; //Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
		double dbl = 123 ; //Stores fractional numbers. Sufficient for storing 15 decimal digits
		boolean bl = true ; //Stores true or false values
		char ch = 'C' ;//Stores a single character/letter or ASCII values
		
		//Non-Premitive data type
		String str = "Nitheesh"; 

	}

}
